def print_params(value):  # Объявление функции print_params с одним параметром value
    print(value)  # Печать значения параметра value
    print(value)  # Повторная печать значения параметра value

# Вызов функции несколько раз с разными аргументами
print_params("Hello")  # Вызов функции с аргументом "Hello"
print_params(123)      # Вызов функции с аргументом 123
print_params([1, 2, 3])# Вызов функции с аргументом [1, 2, 3]